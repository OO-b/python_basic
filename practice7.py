# \" \'  : 문장 내에서 따옴표

# 저는 "나도코딩"입니다.
print("저는 '"'나도코딩'"' 입니다.")
print('저는 "나도코딩" 입니다.')
print("저는 \"나도코딩\" 입니다.")

# \\ : 문장내에서 \
