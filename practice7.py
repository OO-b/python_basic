# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")
print("Reaaaaad Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t :  탭
print("Red\tApple")